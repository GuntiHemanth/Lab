package com.lab2;

public class LeftAlligned_Pattern {

	public static void main(String[] args) {
		
		int i,j,numbeOfRows=6;
		System.out.println("The left alligned star pattern is as follows");
		for(i=0;i<numbeOfRows;i++) {
			for(j=0;j<=i;j++){
				System.out.print("*");
			}
			System.out.println();
		}
	
	}
}
	
		
	
		